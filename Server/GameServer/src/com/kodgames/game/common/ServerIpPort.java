package com.kodgames.game.common;
/**
 * // 用来存放ip和port的类 跟serverType对应
 * */
public class ServerIpPort
{
	private String ip;
	private int port;

	public ServerIpPort()
	{

	}

	public String getIp()
	{
		return ip;
	}

	public void setIp(String ip)
	{
		this.ip = ip;
	}

	public int getPort()
	{
		return port;
	}

	public void setPort(int port)
	{
		this.port = port;
	}
}
